// Helper functions to get products by category
export const getNecklaces = () => products.filter(p => p.category === 'necklace');
export const getBracelets = () => products.filter(p => p.category === 'bracelet');
export const getCombos = () => products.filter(p => p.category === 'combo');
export const getFeaturedProducts = () => products.filter(p => p.isFeatured);
export const getAntiTarnishProducts = () => products.filter(p => p.isAntiTarnish);

// Get product by ID
export const getProductById = (id: string) => products.find(p => p.id === id);

// Get related products (same category, different product)
export const getRelatedProducts = (productId: string, limit: number = 4) => {
  const currentProduct = getProductById(productId);
  if (!currentProduct) return [];
  
  return products
    .filter(p => p.category === currentProduct.category && p.id !== productId)
    .slice(0, limit);
};